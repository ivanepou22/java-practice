public class ConvertStringToIntegerAndViceVasa {
    public static void main(String[] args) {
        //Integer to String
        int x = 65;
        String xString = Integer.toString(x);
        System.out.println(xString);

        //String to Integer
        String y = "65";
        int yInteger = Integer.parseInt(y);
        System.out.println(yInteger);
    }
}
